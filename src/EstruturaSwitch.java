import static java.lang.System.out;

public class EstruturaSwitch {
    public static void main(String[] args) {
        int numero = 2;

        switch (numero) {
            case 1:
                out.println("Você digitou 1");
            break;
            case 2:
                out.println("Você digitou 2");
            break;
        }
    }
}
